    private void Update_EmployeeCombo()
    {
        var sql = "SELECT * FROM Empleado Where inactivo = False";
        try 
        {
          Connection cnn = connect.getCnx();
          Statement stm = cnn.createStatement();
          ResultSet rs = stm.executeQuery(sql);
          DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();

          model.addElement("Seleccione un empleado");
          
            while (rs.next()) 
            {
                int CodigoCliente_ = rs.getInt("CodigoEmpleado");
                String nombre = rs.getString("Nombre");
                String telefono = rs.getString("telefono");
                model.addElement(CodigoCliente_ + ":" + nombre + " - " + telefono);
            }
          combo_empleado.setModel(model);
          
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error retrieving Empleados: " + e.getMessage());
        }
        
    }
