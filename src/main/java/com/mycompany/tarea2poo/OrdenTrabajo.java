    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(this.codigoempleado <= 0)
        {
            JOptionPane.showMessageDialog(null, "Por favor seleccione un empleado");
            return;
        }
        
        Connection cnn = connect.getCnx();
        String observacion = Tobservacion.getText();
        
        String  sql = "Insert into ordendetrabajo (CodigoEmpleado, Observaciones) Values(?,?)";
        try 
        {
          PreparedStatement pst = cnn.prepareStatement(sql);
          pst.setInt(1, this.codigoempleado);
          pst.setString(2, observacion);
          pst.executeUpdate();
        } 
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, "ErrorSQL " + e.getMessage());
        }
        
        if(this.idcliente > 0)
        {
            String query = "SELECT IdOrdenTrabajo from Ordendetrabajo where observaciones = '"+ observacion +"' order by IdOrdenTrabajo desc Limit 1";
            try 
            {
              Statement stm = cnn.createStatement();
              ResultSet rs = stm.executeQuery(query);
              int id = 0;
              if(rs.next())
              {
                  id = rs.getInt("IdOrdenTrabajo");
              }
              if(id > 0)
              {
                String factura = "INSERT into Facturas (CodigoCliente, IdOrdenTrabajo) Values(?,?)";
                PreparedStatement ps = cnn.prepareStatement(factura);
                ps.setInt(1, this.idcliente);
                ps.setInt(2, id);
                ps.executeUpdate();
                cnn.close();
                this.setVisible(false);
              }
            } 
            catch (Exception e)
            {
                JOptionPane.showMessageDialog(null, "ErrorSQL " + e.getMessage());
            }
            
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void combo_empleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combo_empleadoActionPerformed
        String texto = combo_empleado.getSelectedItem().toString();
   
        if ("Seleccione un empleado".equals(texto)) {
            this.codigoempleado = -1;
            return;
        }
        
        String[] partes1 = texto.split(":");

        int id = Integer.parseInt(partes1[0]);
        this.codigoempleado = id;
    }//GEN-LAST:event_combo_empleadoActionPerformed

